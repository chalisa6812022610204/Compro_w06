#include <stdio.h>

int main() {
    int i = 1, j = 2, k; // บรรทัดที่ 1: กำหนดค่าเริ่มต้น i=1, j=2, k ยังไม่มีค่า
    printf("At\t\tExpression\t\ti\tj\tk\n");
    printf("1\t\tstart\t\t   \t%d\t%d\t-\n", i, j); // แสดงค่าเริ่มต้น (k ยังไม่ถูกกำหนด)

    k = i + j;         // บรรทัดที่ 2: k = 1 + 2 = 3
    printf("2\t\tk = i + j\t   \t%d\t%d\t%d\n", i, j, k);

    i = i + (k * j);   // บรรทัดที่ 3: i = 1 + (3 * 2) = 1 + 6 = 7
    printf("3\t\ti = i + (k * j)\t        %d\t%d\t%d\n", i, j, k);

    j = i / 2;         // บรรทัดที่ 4: j = 7 / 2 = 3 (หารจำนวนเต็มตัดเศษ)
    printf("4\t\tj = i / 2\t\t%d\t%d\t%d\n", i, j, k);

    k = i % 2;        // บรรทัดที่ 5: k = 7 % 2 =   1 (เศษจากการหาร)
    printf("5\t\tk = i %% 2\t\t%d\t%d\t%d\n", i, j, k);

    i = (j + k) * 3; // บรรทัดที่ 6: i = (3 + 1) * 3 = 4 * 3 =   12
    printf("6\t\ti = (j + k) * 3\t   \t%d\t%d\t%d\n", i, j, k);

    /*
    ===========================
    อธิบายผลลัพธ์แต่ละบรรทัด
    ===========================
    1. เริ่มต้น: i=1, j=2, k ยังไม่มีค่า
    2. k = i + j = 1 + 2 = 3
    3. i = i + (k * j) = 1 + (3*2) = 1 + 6 = 7
    4. j = i / 2 = 7 / 2 = 3 (หารจำนวนเต็ม ตัดเศษ)
    5. k = i % 2 = 7 % 2 = 1 (เศษจากการหาร 7 ด้วย 2)
    6. i = (j + k) * 3 = (3 + 1) * 3 = 12
    */

    return 0;
}