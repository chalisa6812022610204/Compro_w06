#include <stdio.h>

int main() {
    double x = 1.0, y = 2.0, z; // บรรทัดที่ 1: กำหนดค่าเริ่มต้น x=1.0, y=2.0, z ยังไม่มีค่า
    printf("At\t      Expression\t\t   x\t\ty\t\tz\n");
    printf("-------------------------------------------------------------------\n");
    printf("1\t\tstart\t\t          %.2f  \t%.2f\t\t-\n", x, y);

    x = y + 5.0; // บรรทัดที่ 2: x = 2.0 + 5.0 = 7.0
    printf("2\t\tx = y + 5.0\t\t  %.2f\t\t%.2f\t\t-\n", x, y);

    y = x / 2.0; // บรรทัดที่ 3: y = 7.0 / 2.0 = 3.5
    printf("3\t\ty = x / 2.0\t\t  %.2f\t\t%.2f\t\t-\n", x, y);

    y = (x * 3.0) + 4.0; // บรรทัดที่ 4: y = (7.0 * 3.0) + 4.0 = 21.0 + 4.0 = 25.0
    printf("4\t\ty = (x * 3.0) + 4.0\t  %.2f\t\t%.2f\t\t-\n", x, y);

    x = -0.5 - y; // บรรทัดที่ 5: x = -0.5 - 25.0 = -25.5
    printf("5\t\tx = -0.5 - y\t\t %.2f\t\t%.2f\t\t-\n", x, y);

    z = x + y; // บรรทัดที่ 6: z = -25.5 + 25.0 = -0.5
    printf("6\t\tz = x + y\t\t %.2f\t\t%.2f\t\t%.2f\n", x, y, z);

    /*
    ===========================
    อธิบายผลลัพธ์แต่ละบรรทัด
    ===========================
    1. เริ่มต้น: x=1.0, y=2.0, z ยังไม่มีค่า
    2. x = y + 5.0 = 2.0 + 5.0 = 7.0
    3. y = x / 2.0 = 7.0 / 2.0 = 3.5
    4. y = (x * 3.0) + 4.0 = (7.0 * 3.0) + 4.0 = 21.0 + 4.0 = 25.0
    5. x = -0.5 - y = -0.5 - 25.0 = -25.5
    6. z = x + y = -25.5 + 25.0 = -0.5
    */

    return 0;
}