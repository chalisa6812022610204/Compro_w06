#include <stdio.h>
#include <math.h> // ใช้ฟังก์ชัน fmod() สำหรับการหาเศษของทศนิยม

int main() {
    // กำหนดค่าตัวแปรเริ่มต้น
    int a = 5;       // จำนวนเต็ม a
    int b = 17;      // จำนวนเต็ม b
    float c = 8.5;   // จำนวนจริง c
    float d = 4.0;   // จำนวนจริง d

    // แสดงหัวตาราง
    printf("บรรทัดที่\tผลลัพธ์\n");
    printf("-------------------------\n");
    // 1. แสดงค่าเริ่มต้นของตัวแปรทั้งหมด
    printf("1\t\t a = %d, b = %d, c = %.2f, d = %.2f\n", a, b, c, d);
    // 2. d + a
    printf("2\t\t %.2f\n", d + a);
    // 3. a - b
    printf("3\t\t %d\n", a - b);
    // 4. c * d
    printf("4\t\t %.2f\n", c * d);
    // 5. a * c
    printf("5\t\t %.2f\n", a * c);
    // 6. c / d
    printf("6\t\t %.2f\n", c / d);
    // 7. b / c
    printf("7\t\t %.2f\n", b / c);
    // 8. a % b (ใช้ได้เพราะเป็นจำนวนเต็ม)
    printf("8\t\t %d\n", a % b);
    // 9. c % a (ใช้ fmod เพราะ c เป็น float)
    printf("9\t\t %.2f\n", fmod(c, a));
    // 10. c % d (ใช้ fmod เพราะเป็น float ทั้งคู่)
    printf("10\t\t %.2f\n", fmod(c, d));
    /*
    ===========================
    อธิบายผลลัพธ์แต่ละบรรทัด
    ===========================
    1. แสดงค่าเริ่มต้นของตัวแปรทั้งหมด -> a=5, b=17, c=8.50, d=4.00
    2. d + a = 4.0 + 5 = 9.0 -> ผลลัพธ์เป็นทศนิยม
    3. a - b = 5 - 17 = -12 -> ผลลัพธ์เป็นจำนวนเต็มติดลบ
    4. c * d = 8.5 * 4.0 = 34.0 -> คูณค่าทศนิยม
    5. a * c = 5 * 8.5 = 42.5 -> คูณจำนวนเต็มกับทศนิยม
    6. c / d = 8.5 / 4.0 = 2.125 -> หารทศนิยม
    7. b / c = 17 / 8.5 = 2.0 -> หารแล้วได้ทศนิยมที่เป็นจำนวนเต็มพอดี
    8. a % b = 5 % 17 = 5 -> หาเศษจากการหารจำนวนเต็ม
    9. c % a = fmod(8.5, 5) = 3.5 -> เศษจากการหาร 8.5 ด้วย 5
    10. c % d = fmod(8.5, 4.0) = 0.5 -> เศษจากการหาร 8.5 ด้วย 4.0
    */

    return 0;
}